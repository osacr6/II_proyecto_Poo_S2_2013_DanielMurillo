    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if(IDpregunta==-1)
        {
            IDpregunta=Instacia.SET_pregunta(IDexamen,IDsecccion,jTextArea1.getText(),Integer.parseInt(jSpinner1.getValue().toString()));
           if((IDpregunta>-1)&&(jTextArea2.getText()!=""))
           {
               Instacia.SET_Respuesta(IDexamen,IDsecccion,IDpregunta,jTextArea2.getText(),jCheckBox1.isSelected());
               jCheckBox1.setSelected(false);
               jTable1.setModel(
                       Instacia.get_Respuestas(IDexamen,IDsecccion,IDpregunta)  
                       );
              jLabel8.setText(String.valueOf(Instacia.seccion_size(IDexamen,IDsecccion)));
              jTextArea2.setText("");
           }
        }
        else
        {
            if((IDpregunta>-1)&&(jTextArea2.getText()!=""))
           {
               Instacia.SET_Respuesta(IDexamen,IDsecccion,IDpregunta,jTextArea2.getText(),jCheckBox1.isSelected());
               jCheckBox1.setSelected(false);
               jTable1.setModel(
                       Instacia.get_Respuestas(IDexamen,IDsecccion,IDpregunta)  
                       );               
                jLabel8.setText(String.valueOf(Instacia.seccion_size(IDexamen,IDsecccion)));
           }
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row = jTable1.rowAtPoint(evt.getPoint());
        int col = jTable1.columnAtPoint(evt.getPoint());
        if (row >= 0 && col >= 0) {
             IDrespuesta=Integer.parseInt(jTable1.getValueAt(row,0).toString());
             jTextArea2.setText(jTable1.getValueAt(row,1).toString());
             jButton4.setText("Editar");
             if(jTable1.getValueAt(row,2).toString()=="true")
                 jCheckBox1.setSelected(true);
        }
        if(SwingUtilities.isRightMouseButton(evt)){
            if(IDrespuesta>-1)
            {
                Instacia.delete_Respuesta(IDexamen,IDsecccion,IDpregunta,IDrespuesta);
                jTable1.setModel(
                       Instacia.get_Respuestas(IDexamen,IDsecccion,IDpregunta)  
                       );
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         if((IDsecccion>-1)&&(IDexamen>-1))
         {
            if(Integer.parseInt(jLabel6.getText()) < Integer.parseInt(jLabel8.getText()))
            {
                if(Integer.parseInt(jLabel6.getText())+1 == Integer.parseInt(jLabel8.getText()))
                {
                    IDpregunta=-1;
                    jTextArea1.setText("");
                    jLabel6.setText(String.valueOf(Integer.parseInt(jLabel6.getText())+1));
                    jLabel8.setText(String.valueOf(Instacia.seccion_size(IDexamen,IDsecccion)));
                    jTable1.setModel(Instacia.get_Respuestas(-1,-1,-1));
                }
                else
                {
                     jLabel6.setText(String.valueOf(Integer.parseInt(jLabel6.getText())+1));
                     jLabel8.setText(String.valueOf(Instacia.seccion_size(IDexamen,IDsecccion)));
                     jTextArea1.setText(Instacia.Get_pregunta(IDexamen,IDsecccion,Integer.parseInt(jLabel6.getText())));
                     jTable1.setModel(Instacia.get_Respuestas(IDexamen,IDsecccion,Integer.parseInt(jLabel6.getText())));
                 }
            }
         }
    }//GEN-LAST:event_jButton2ActionPerformed

